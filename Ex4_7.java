
public class Ex4_7 {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        int sum = a + b;
            System.out.println("Сумма a и b равна " + sum);

        int diff = a - b;
            System.out.println("Разность a и b равна " + diff);

        int product = a * b;
            System.out.println("Произведение a и b равно " + product);

        int quotient = a / b;
            System.out.println("Результат деления a на b равен " + quotient);

        int remainder = a % b;
            System.out.println("Остаток от деления a на b равен " + remainder);
    }
}

